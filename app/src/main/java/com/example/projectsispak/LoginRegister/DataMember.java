package com.example.projectsispak.LoginRegister;

public class DataMember {
    private String namaMember;
    private String namaKucing;
    private String jenisKucing;
    private String beratKucing;
    private String umurKucing;

    public String getNamaMember() {
        return namaMember;
    }

    public void setNamaMember(String namaMember) {
        this.namaMember = namaMember;
    }

    public String getNamaKucing() {
        return namaKucing;
    }

    public void setNamaKucing(String namaKucing) {
        this.namaKucing = namaKucing;
    }

    public String getJenisKucing() {
        return jenisKucing;
    }

    public void setJenisKucing(String jenisKucing) {
        this.jenisKucing = jenisKucing;
    }

    public String getBeratKucing() {
        return beratKucing;
    }

    public void setBeratKucing(String beratKucing) {
        this.beratKucing = beratKucing;
    }

    public String getUmurKucing() {
        return umurKucing;
    }

    public void setUmurKucing(String umurKucing) {
        this.umurKucing = umurKucing;
    }
}
